    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        // Tomar n√∫meros
        String n1 = txtNum1.getText();
        String n2 = txtNum2.getText();

        // XML de la SUMA
        String xml =
            "<soap:Envelope xmlns:xsi=\"http://www.w3.org/2001/XMLSchema-instance\" "
          + "xmlns:xsd=\"http://www.w3.org/2001/XMLSchema\" "
          + "xmlns:soap=\"http://schemas.xmlsoap.org/soap/envelope/\">"
          + "<soap:Body>"
          + "<Suma xmlns=\"http://tempuri.org/\">"
          + "<num1>" + n1 + "</num1>"
          + "<num2>" + n2 + "</num2>"
          + "</Suma>"
          + "</soap:Body>"
          + "</soap:Envelope>";

        // Llamar WS
        String response = "";
        try {
            response = ClienteSOAP.enviarSOAP("http://tempuri.org/Suma", xml);
        } catch (Exception e) { }

        // Extraer resultado
        String inicio = "<SumaResult>";
        String fin = "</SumaResult>";

        String resultado = response.substring(
                response.indexOf(inicio) + inicio.length(),
                response.indexOf(fin)
        );

        // Mostrar en tu textfield
        txtResultado.setText(resultado);
    }//GEN-LAST:event_btnCalcularActionPerformed

