    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        int numero = Integer.parseInt(txtNum1.getText());

        // Construir XML para enviar
        String xml =
            "<soap:Envelope xmlns:xsi=\"http://www.w3.org/2001/XMLSchema-instance\" "
          + "xmlns:xsd=\"http://www.w3.org/2001/XMLSchema\" "
          + "xmlns:soap=\"http://schemas.xmlsoap.org/soap/envelope/\">"
          + "<soap:Body>"
          + "<Tabla1 xmlns=\"http://tempuri.org/\">"
          + "<numero>" + numero + "</numero>"
          + "</Tabla1>"
          + "</soap:Body>"
          + "</soap:Envelope>";

        String response = "";
        try {
            response = ClienteSOAP.enviarSOAP("http://tempuri.org/Tabla1", xml);
        } catch (Exception e) { }

        // Extraer contenido dentro de <Tabla1Result>...</Tabla1Result>
        String inicio = "<Tabla1Result>";
        String fin = "</Tabla1Result>";

        String text = response.substring(
                response.indexOf(inicio) + inicio.length(),
                response.indexOf(fin)
        );

        // Reconstruir cada valor de multiplicaci√≥n (porque vienen pegados)
        ArrayList<String> lista = new ArrayList<>();

        int index = 0;
        for (int i = 0; i <= 12; i++) {
            int valor = numero * i;
            String v = String.valueOf(valor);
            int len = v.length();

            lista.add(numero + " x " + i + " = " + v);
            index += len;
        }

        // Mostrar en el TextArea
        txtResultado.setText(""); // limpiar

        for (String linea : lista) {
            txtResultado.append(linea + "\n");
        }
    }//GEN-LAST:event_btnCalcularActionPerformed

